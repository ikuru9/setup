function inferLanguageId(filePath) {
  const extension = extname(filePath).toLowerCase();
  const known = {
    ".ts": "typescript", ".tsx": "typescriptreact", ".js": "javascript", ".jsx": "javascriptreact",
    ".py": "python", ".rs": "rust", ".go": "go", ".java": "java", ".kt": "kotlin",
    ".c": "c", ".h": "c", ".cc": "cpp", ".cpp": "cpp", ".hpp": "cpp",
    ".json": "json", ".yaml": "yaml", ".yml": "yaml", ".html": "html", ".css": "css",
  };
  return known[extension] || extension.replace(/^\./, "") || "plaintext";
}

function languageIdForFile(server, filePath) {
  const extension = extname(filePath).toLowerCase();
  const mapping = server.extensionToLanguage && typeof server.extensionToLanguage === "object"
    ? server.extensionToLanguage
    : {};
  for (const [candidate, languageId] of Object.entries(mapping)) {
    if (candidate === "*") continue;
    const normalized = String(candidate).toLowerCase();
    if ((normalized.startsWith(".") ? normalized : "." + normalized) === extension) return languageId;
  }
  return mapping["*"] || server.languageId || inferLanguageId(filePath);
}

function chooseLspServer(filePath) {
  const extension = extname(filePath).toLowerCase();
  return LSP_SERVERS.find((server) => {
    const extensions = server.extensions && server.extensions.length > 0
      ? server.extensions
      : Object.keys(server.extensionToLanguage || {}).filter((candidate) => candidate !== "*");
    if (extensions.length === 0) return true;
    return extensions.some((candidate) => {
      const normalized = String(candidate).toLowerCase();
      return normalized === extension || (normalized.startsWith(".") ? normalized : "." + normalized) === extension;
    });
  });
}

