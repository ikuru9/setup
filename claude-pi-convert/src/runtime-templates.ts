async function runConvertedHooks(eventName, payload, ctx, signal) {
  const aggregate: HookDecision = { block: false, reason: "", updatedInput: undefined, updatedResult: undefined, permission: undefined, updatedPrompt: undefined, additionalContext: [] };
  for (const hook of HOOKS) {
    if (hook.event !== eventName || !matchesHook(hook, payload)) continue;
    for (const handler of hook.handlers) {
      let decision;
      if (handler.type === "command") decision = await runCommandHook(handler, payload, ctx, signal);
      else if (handler.type === "http") decision = await runHttpHook(handler, payload, ctx, signal);
      else if (handler.type === "prompt") decision = await runPromptHook(handler, payload, ctx, signal);
      else {
        continue;
      }
      mergeDecision(aggregate, decision);
      if (aggregate.block) return aggregate;
    }
  }
  return aggregate;
}

