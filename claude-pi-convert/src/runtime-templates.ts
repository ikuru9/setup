async function runCommandHook(handler, payload, ctx, signal) {
  if (!handler.command) return parseHookOutput("", 0);
  const useShell = handler.shell !== false;
  const command = useShell ? String(handler.command) : expandValue(handler.command, ctx);
  const executable = useShell
    ? process.platform === "win32" ? (process.env.ComSpec || "cmd.exe") : "/bin/sh"
    : command;
  const args = useShell
    ? process.platform === "win32" ? ["/d", "/s", "/c", command] : ["-lc", command]
    : (handler.args || []).map((arg) => expandValue(String(arg), ctx));
  const env = childEnvironment(handler.env, ctx);
  const child = spawn(executable, args, {
    cwd: resolveWorkingDirectory(handler.cwd, ctx),
    env,
    detached: useShell && process.platform !== "win32",
    shell: false,
    stdio: ["pipe", "pipe", "pipe"],
    windowsHide: true,
  });
  const terminate = (signalName = "SIGTERM") => {
    if (!child.pid || child.exitCode !== null || child.signalCode !== null) return;
    if (useShell && process.platform !== "win32") {
      try {
        process.kill(-child.pid, signalName);
        return;
      } catch {}
    }
    try { child.kill(signalName); } catch {}
  };
  child.stdin.on("error", (error) => {
    if (error && error.code !== "EPIPE") console.error("[" + PLUGIN_SLUG + "] hook stdin:", error);
  });
  child.stdin.end(JSON.stringify(payload));
  if (handler.async) {
    child.stdout.resume();
    child.stderr.on("data", (chunk) => console.error("[" + PLUGIN_SLUG + "] async hook:", String(chunk).trimEnd()));
    child.unref();
    return parseHookOutput("", 0);
  }
  return await new Promise((resolvePromise) => {
    let stdout = "";
    let stderr = "";
    let settled = false;
    const timeoutMs = Math.max(1, Number(handler.timeoutMs) || 60_000);
    const timer = setTimeout(() => {
      terminate();
      finish(124, new Error("Hook timed out after " + timeoutMs + "ms"));
    }, timeoutMs);
    const onAbort = () => {
      terminate();
      finish(130, new Error("Hook aborted"));
    };
    const finish = (code, error) => {
      if (settled) return;
      settled = true;
      clearTimeout(timer);
      if (signal) signal.removeEventListener("abort", onAbort);
      if (stderr.trim()) console.error("[" + PLUGIN_SLUG + "] hook stderr:", stderr.trimEnd());
      if (error) {
        console.error("[" + PLUGIN_SLUG + "] hook failed:", error.message || error);
        notify(ctx, "Hook failed: " + (error.message || error), "warning");
      }
      resolvePromise(parseHookOutput(stdout, code));
    };
    child.stdout.on("data", (chunk) => { stdout = appendLimited(stdout, chunk); });
    child.stderr.on("data", (chunk) => { stderr = appendLimited(stderr, chunk); });
    child.once("error", (error) => finish(1, error));
    child.once("close", (code) => finish(code === null ? 1 : code));
    if (signal) {
      if (signal.aborted) onAbort();
      else signal.addEventListener("abort", onAbort, { once: true });
    }
  });
}

