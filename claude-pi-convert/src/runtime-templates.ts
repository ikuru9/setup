function originalPluginRoot(ctx) {
  if (existsSync(PACKAGE_ORIGINAL_PLUGIN_ROOT)) return PACKAGE_ORIGINAL_PLUGIN_ROOT;
  const runtimeRoot = activePluginRuntimeRoot(ctx);
  const activeRoot = runtimeRoot ? resolve(runtimeRoot, "original") : undefined;
  return activeRoot && existsSync(activeRoot) ? activeRoot : PACKAGE_ORIGINAL_PLUGIN_ROOT;
}

function pluginDataRoot(ctx) {
  const runtimeRoot = activePluginRuntimeRoot(ctx);
  return runtimeRoot && existsSync(runtimeRoot) ? resolve(runtimeRoot, "data") : PACKAGE_PLUGIN_DATA_ROOT;
}

function pluginRuntimeBinRoot(ctx) {
  const runtimeRoot = activePluginRuntimeRoot(ctx);
  return runtimeRoot && existsSync(runtimeRoot) ? resolve(runtimeRoot, "bin") : PACKAGE_RUNTIME_BIN_ROOT;
}

function prependPathEntries(currentPath, entries) {
  const normalize = (entry) => process.platform === "win32"
    ? String(entry).replace(/\\/g, "/").toLowerCase()
    : String(entry);
  const seen = new Set<string>();
  const output: string[] = [];
  for (const entry of [...entries, ...String(currentPath || "").split(delimiter)]) {
    if (!entry) continue;
    const key = normalize(entry);
    if (seen.has(key)) continue;
    seen.add(key);
    output.push(entry);
  }
  return output.join(delimiter);
}

function expandValue(value, ctx, extra = {}) {
  if (typeof value !== "string") return value;
  const vars = {
    ...process.env,
    CLAUDE_PLUGIN_ROOT: originalPluginRoot(ctx),
    CLAUDE_PLUGIN_DATA: pluginDataRoot(ctx),
    CLAUDE_PROJECT_DIR: ctx && ctx.cwd ? ctx.cwd : process.cwd(),
    ...extra,
  };
  return value.replace(/\$\{([A-Za-z_][A-Za-z0-9_]*)\}|\$env:([A-Za-z_][A-Za-z0-9_]*)|\$([A-Za-z_][A-Za-z0-9_]*)/gi, (match, braced, windowsEnv, plain) => {
    const key = braced || windowsEnv || plain;
    const replacement = vars[key];
    return replacement === undefined ? match : String(replacement);
  });
}

function resolveWorkingDirectory(value, ctx) {
  if (!value) return ctx && ctx.cwd ? ctx.cwd : process.cwd();
  const expanded = expandValue(value, ctx);
  return isAbsolute(expanded) ? expanded : resolve(ctx && ctx.cwd ? ctx.cwd : process.cwd(), expanded);
}

function childEnvironment(configuredEnv, ctx, inheritProcess = true) {
  const env = inheritProcess ? { ...process.env } : {};
  for (const [key, value] of Object.entries(configuredEnv || {})) env[key] = expandValue(String(value), ctx);
  const pluginRoot = originalPluginRoot(ctx);
  env.CLAUDE_PLUGIN_ROOT = pluginRoot;
  env.CLAUDE_PLUGIN_DATA = pluginDataRoot(ctx);
  env.CLAUDE_PROJECT_DIR = ctx && ctx.cwd ? ctx.cwd : process.cwd();
  const configuredPathKey = Object.keys(configuredEnv || {}).find((key) => key.toLowerCase() === "path");
  const inheritedPathKey = Object.keys(process.env).find((key) => key.toLowerCase() === "path");
  const pathKey = configuredPathKey || inheritedPathKey || "PATH";
  const currentPath = env[pathKey] === undefined ? process.env[pathKey] : env[pathKey];
  for (const key of Object.keys(env)) {
    if (key !== pathKey && key.toLowerCase() === "path") delete env[key];
  }
  env[pathKey] = prependPathEntries(currentPath, [pluginRuntimeBinRoot(ctx), resolve(pluginRoot, "bin")]);
  return env;
}

function shellSingleQuote(value) {
  const apostrophe = String.fromCharCode(39);
  const doubleQuote = String.fromCharCode(34);
  return apostrophe + String(value).split(apostrophe)
    .join(apostrophe + doubleQuote + apostrophe + doubleQuote + apostrophe) + apostrophe;
}

function powershellSingleQuote(value) {
  const apostrophe = String.fromCharCode(39);
  return apostrophe + String(value).split(apostrophe).join(apostrophe + apostrophe) + apostrophe;
}

function prefixBashToolPath(event, ctx) {
  if (!event || event.toolName !== "bash" || !event.input || typeof event.input.command !== "string") return;
  if (event.input.command.includes(BASH_PATH_MARKER)) return;
  const prefix = [pluginRuntimeBinRoot(ctx), resolve(originalPluginRoot(ctx), "bin")].join(delimiter) + delimiter;
  const pathCommand = process.platform === "win32"
    ? "$env:Path = " + powershellSingleQuote(prefix) + " + $env:Path"
    : "export PATH=" + shellSingleQuote(prefix) + '"$PATH"';
  event.input.command = BASH_PATH_MARKER + "\n" + pathCommand + "\n" + event.input.command;
}

function appendLimited(current, chunk) {
  if (current.length >= MAX_HOOK_OUTPUT) return current;
  const text = typeof chunk === "string" ? chunk : chunk.toString("utf8");
  return (current + text).slice(0, MAX_HOOK_OUTPUT);
}

